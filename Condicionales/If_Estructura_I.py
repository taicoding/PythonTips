condicion = True
#Estructura de la condicional 'if'
if condicion:
    #Si la condicion es verdadera 
    #realizamos una accion
    print('Se cumple la condicion')
else:
    #Si la condicion es falsa 
    #podemos realizar otra accion
    print('No se cumple la condicion')
#Resultado: Se cumple la condicion
