#Definimos las variables con las que realizaremos 
#las operaciones aritmeticas
exponente = 2
base = 3 
#¿Como calculamos potencias?
#El operador que nos permite calcular
#potencias es el '**' 
potencia = base ** exponente
print(potencia)
#Resultado: 9
#¿Y como represento la potencia de otra potencia?
#Puedes representarlo de la siguiente forma:
uwu_exponente = 3
uwu_potencia = (base ** exponente) ** uwu_exponente
#Recuerda siempre usar sabiamente los (parentesis) 
print(uwu_potencia)
#Resultado: 729
