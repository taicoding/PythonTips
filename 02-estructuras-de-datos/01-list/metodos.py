# Lista inicial
emociones = ["uwu"]
# ⭐️ Agregar un elemento ⭐️
# append(elemento)
emociones.append("owo")
print(emociones)
# R: ['uwu', 'owo']
# ⭐️ Agregar un elemento ⭐️
# insert(indice,elemento)
emociones.insert(1, "e_e")
print(emociones)
# R: ['uwu', 'e_e', 'owo']
# concatenacion de listas
emociones = emociones + ["o_o"]
print(emociones)
# R: ['uwu', 'e_e', 'owo', 'o_o']
# ⭐️ Tip
# ⭐️ append es la forma mas rapida
