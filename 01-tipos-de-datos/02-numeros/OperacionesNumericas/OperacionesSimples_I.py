# Definimos las variables con las que realizaremos
# las operaciones aritmeticas
a, b = 8, 3
# La adiccion o suma se realiza con
# el operador '+'
suma = a + b
print(suma)
# Resultado: 11
# La sustraccion o resta se realiza con
# el operador '-'
resta = a - b
print(resta)
# Resultado: 5
