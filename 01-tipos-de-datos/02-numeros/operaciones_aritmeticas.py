a, b = 3, 2
print("Exponenciación:", a ** b)
# Exponenciación: 9
print("Multiplicación:", a * b)
# Multiplicación: 6
print("División:", a / b)
# División: 1.5
print("División entera:", a // b)
# División entera: 1
print("Modulo:", a % b)
# Modulo: 1
print("Adición:", a + b)
# Adición: 5
print("Sustracción:", a - b)
# Sustracción: 1
